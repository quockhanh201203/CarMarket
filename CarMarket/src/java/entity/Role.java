package entity;

/**
 *
 * @author admin
 */
public class Role {
    private int RoleId;
    private String roleName;

    public Role() {
    }

    public Role(int RoleId, String roleName) {
        this.RoleId = RoleId;
        this.roleName = roleName;
    }

    public int getRoleId() {
        return RoleId;
    }

    public void setRoleId(int RoleId) {
        this.RoleId = RoleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }
    
    
}
